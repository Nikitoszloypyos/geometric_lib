def area(a):
    """Находит площадь квадрата.

    Параметры:
        a (float): длина стороны квадрата.

    Возвращаемые значения:
        float: площадь квадрата.
    """
    return a * a


def perimeter(a):
    """Находит периметр квадрата.

    Параметры:
        a (float): длина стороны квадрата.

    Возвращаемые значения:
        float: периметр квадрата.
    """
    return 4 * a
